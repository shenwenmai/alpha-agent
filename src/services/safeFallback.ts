// ============================================================
// safeFallback — 拦截命中后的固定回复模板
// 不走 LLM 二次生成，确定性输出
// ============================================================

export type GuardReason =
  | "gambling_strategy"
  | "off_topic"
  | "fact_conflict"
  | "high_distress"
  | "crisis"
  | "resistance"
  | "default";

export function getSafeFallback(reason: GuardReason): string {
  switch (reason) {
    case "gambling_strategy":
      return [
        "我不能帮你优化任何赌博/翻本策略（包括凯利这类方法）。",
        "我更在意你现在不再被下一把拖走。先只做一步：今晚先暂停24小时不入场。",
        "你愿意现在就确认\u201c今晚不下单\u201d，还是你觉得做不到？",
      ].join("\n");
    case "off_topic":
      return [
        "我先把话题拉回你现在最重要的事：把这次波动稳住。",
        "先不展开别的话题，我们只做一步：你现在这轮到目前是净赚还是净亏？",
        "给我一个结果（净赚 / 净亏 + 大概金额）就行。",
      ].join("\n");
    case "fact_conflict":
      return [
        "我注意到前后信息有点冲突，我先不做判断。",
        "先把事实对齐：你说\u201c昨天赚了很多\u201d，同时又提到\u201c往肚子里吞\u201d。",
        "所以这轮到现在到底是净赚还是净亏？大概多少？",
      ].join("\n");
    case "high_distress":
      return [
        "我听到你现在是在硬扛，压力很大。",
        "先不讲道理，只做一个保护动作：今晚把交易/下注入口先停24小时。",
        "你现在能做的是哪一个：A 立刻停  B 需要我陪你先停1小时？",
      ].join("\n");
    case "crisis":
      return [
        "我现在最关心你的安全。请你先告诉我：你现在是一个人吗？身边有没有可能伤害自己的工具？",
        "如果有立即风险，请马上联系当地急救/紧急热线，或立刻联系一个你信任的人到你身边。",
        "你先回复我\u201c我现在是否安全（是/否）\u201d。",
      ].join("\n");
    case "resistance":
      return [
        "可以不细聊细节，我们先做最小一步就好。",
        "给我一个范围，这次大概是亏了 1k内 / 1k\u20131w / 1w+ ？",
      ].join("\n");
    default:
      return [
        "我在这，先陪你把这一步走稳。",
        "我们先做一个最小动作：你现在这轮是净赚还是净亏？",
        "回复我\u201c净赚/净亏 + 大概金额\u201d就可以。",
      ].join("\n");
  }
}
